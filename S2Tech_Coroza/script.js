document.getElementById('monday').addEventListener('click', function() {
    var box = document.getElementById('box');
    box.style.display = 'block';
    setTimeout(function() {
      box.classList.add('move');
      setTimeout(function() {
        box.classList.add('change-color');
      }, 1000);
    }, 0);
  });
  
